#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	int		taille;

	taille = 0;
	i = 0;
	while (s[taille])
		taille++;
	dest = malloc(sizeof(char) * (taille + 1));
	if (dest == NULL)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
