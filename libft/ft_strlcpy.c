#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	taille;

	taille = 0;
	i = 0;
	while (src[taille])
		taille++;
	if (size == 0)
		return (taille);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (taille);
}
